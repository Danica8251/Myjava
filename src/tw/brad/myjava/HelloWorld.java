package tw.brad.myjava;  
// Java 提供「套件」（package）來管理類別
// 橘色為Java的關鍵字，總共有50幾個，永遠都全小寫
// 結束時要加 ; ，表示此句結束

public class HelloWorld {
// public 存取修飾字，public class 存取的類別
// 類別名稱要跟檔案名稱一樣，包含大小寫，稱為主類別
// Java 大小寫嚴格區分
// { } 表該類別實作的區域(要做的事情)
// 寫成是要習慣縮排，縮4排(tab按 2 次)
	
		public static void main (String[ ] args) {		// public static：程式執行進入點
				System.out.println("Hello, World");
				System.out.println("賴羽薇");
		}
		
}

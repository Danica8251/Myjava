package tw.brad.myjava;

public class Brad07 {

	public static void main(String[] args) {
		// 2^16 => 0 ~ 65535
		char c1 = 'a';		// 字元用 ' ' 表示，是一個字元，" " 是字串
		char c2 = 97;
		char c3 = 65;
		char c4 = '資';
		System.out.println(c1);
		System.out.println(c2);		// 輸出 a ，可以參照 ascii code表
		System.out.println(c3);		// 輸出 A
		System.out.println(c4); 	// 輸出 資		
	}

}
